using System.Collections;
using System.Reflection;
using System.Text;
using DbspNet.Sql.Optimizer;
using DbspNet.Sql.Parser;
using DbspNet.Sql.Plan;

namespace DbspNet.Benchmarks;

/// <summary>
/// Research instrument for <c>docs/research-calcite-rules.md</c>: counts, over the
/// SQL corpora this repo can compile, how many <i>sites</i> each candidate Calcite
/// rewrite rule would fire on — measured against the plan the optimizer already
/// produces (post-<see cref="PlanOptimizer.Optimize"/>, so a site here is a site
/// the current pass set genuinely leaves on the table).
/// </summary>
/// <remarks>
/// <para><b>Reading the numbers.</b> A site count is an <i>upper bound on
/// applicability</i>, not a win: it says the pattern occurs, not that rewriting it
/// pays. Each detector documents what it over- and under-counts. Zero sites is the
/// strongest signal the census gives — a rule with no sites cannot pay on this
/// corpus, whatever Calcite's cost model thinks of it.</para>
/// <para><b>Shared subtrees.</b> The optimizer's final CSE pass turns the plan into
/// a DAG. Nodes are visited once by reference (a subtree shared by two consumers
/// counts one site), which matches what a rewrite would actually rewrite.</para>
/// <para><b>Corpus.</b> Nexmark (q0–q22, whichever compile) plus the fraud
/// feature view. Both are join/aggregate/window shaped. The analytic shapes Calcite
/// rules were mostly written for — constant-folded dbt boilerplate, CASE-sums,
/// nullable keys — live in the batch-1 dbt program, which is not on this host
/// (see the <c>columnar-batch1-tooling-portability</c> note); re-run this census
/// there before trusting a "zero sites" verdict for those families.</para>
/// </remarks>
internal static class CalciteRuleCensus
{
    public static void Run(StringBuilder o)
    {
        var corpus = new List<(string Query, LogicalPlan Plan)>();
        var failed = new List<(string Query, string Reason)>();

        foreach (var q in Nexmark.NexmarkQueries.All)
        {
            try
            {
                corpus.Add((q.Id, BuildOptimized(Nexmark.NexmarkQueries.Ddl, q.Sql)));
            }
            catch (Exception ex)
            {
                failed.Add((q.Id, ex.GetType().Name + ": " + FirstLine(ex.Message)));
            }
        }

        try
        {
            corpus.Add(("fraud", BuildOptimized(Fraud.FraudBenchmark.Ddl, Fraud.FraudBenchmark.FeatureSql)));
        }
        catch (Exception ex)
        {
            failed.Add(("fraud", ex.GetType().Name + ": " + FirstLine(ex.Message)));
        }

        // rule -> query -> site count.
        var counts = new Dictionary<string, Dictionary<string, int>>(StringComparer.Ordinal);
        var examples = new Dictionary<string, List<string>>(StringComparer.Ordinal);

        foreach (var (query, plan) in corpus)
        {
            foreach (var site in Census(plan))
            {
                var perQuery = counts.TryGetValue(site.Rule, out var d)
                    ? d
                    : counts[site.Rule] = new Dictionary<string, int>(StringComparer.Ordinal);
                perQuery[query] = perQuery.GetValueOrDefault(query) + 1;
                // Keep a couple of distinct examples per rule — one is often unrepresentative.
                var seenExamples = examples.TryGetValue(site.Rule, out var list)
                    ? list
                    : examples[site.Rule] = new List<string>();
                var rendered = $"{query}: {site.Detail}";
                if (seenExamples.Count < 2 && !seenExamples.Contains(rendered))
                {
                    seenExamples.Add(rendered);
                }
            }
        }

        o.AppendLine("# Calcite rule-applicability census");
        o.AppendLine();
        o.AppendLine(
            "Generated by `dotnet run --project src/DbspNet.Benchmarks -c Release -- rulecensus`. " +
            "Counts *sites* where a candidate Calcite rewrite would match the plan " +
            "**after** `PlanOptimizer.Optimize` — i.e. redundancy the current pass set " +
            "leaves behind. A site is an upper bound on applicability, not a measured win; " +
            "see `docs/research-calcite-rules.md` for the triage and what each count is " +
            "worth under incremental (as opposed to batch) execution.");
        o.AppendLine();
        o.AppendLine($"**Corpus:** {corpus.Count} plans ({string.Join(", ", corpus.Select(c => c.Query))}).");
        if (failed.Count > 0)
        {
            o.AppendLine();
            o.AppendLine("**Did not compile** (excluded from the census):");
            o.AppendLine();
            foreach (var (q, reason) in failed)
            {
                o.AppendLine($"- `{q}` — {reason}");
            }
        }

        o.AppendLine();
        o.AppendLine("## Sites per rule");
        o.AppendLine();
        o.AppendLine("| candidate rule | sites | queries | examples |");
        o.AppendLine("|:--|--:|:--|:--|");

        foreach (var rule in Detectors.Select(d => d.Name))
        {
            var perQuery = counts.GetValueOrDefault(rule);
            var total = perQuery?.Values.Sum() ?? 0;
            var qs = perQuery is null
                ? "—"
                : string.Join(", ", perQuery.OrderByDescending(kv => kv.Value)
                    .ThenBy(kv => kv.Key, StringComparer.Ordinal)
                    .Select(kv => kv.Value == 1 ? kv.Key : $"{kv.Key}×{kv.Value}"));
            var ex = examples.TryGetValue(rule, out var exs) ? string.Join("; ", exs) : "—";
            o.AppendLine($"| `{rule}` | {total} | {qs} | {ex} |");
        }

        o.AppendLine();
        o.AppendLine("## What each detector matches (and what it misses)");
        o.AppendLine();
        foreach (var d in Detectors)
        {
            o.AppendLine($"- **`{d.Name}`** — {d.What}");
        }

        Console.WriteLine(o.ToString());
    }

    // ---------- detectors ----------

    private sealed record Site(string Rule, string Detail);

    private sealed record Detector(string Name, string What, Func<LogicalPlan, LogicalPlan?, IEnumerable<Site>> Match);

    private static readonly Detector[] Detectors =
    {
        new(
            "FILTER_AGGREGATE_TRANSPOSE",
            "`Filter` directly over `Aggregate` whose conjunct references only group-key columns " +
            "(output index < GroupKeys.Count), so it could run below the aggregate. Under-counts: " +
            "filters separated from the aggregate by a projection the optimizer did not fuse. " +
            "Over-counts: nothing — group-key-only conjuncts are unconditionally pushable.",
            (n, _) =>
            {
                if (n is not FilterPlan { Input: AggregatePlan agg } f)
                {
                    return Array.Empty<Site>();
                }

                return ExpressionRewriter.SplitAnd(f.Predicate)
                    .Where(c =>
                    {
                        var refs = ExpressionRewriter.CollectColumnIndices(c);
                        return refs.Count > 0 && refs.All(i => i < agg.GroupKeys.Count);
                    })
                    .Select(_ => new Site("FILTER_AGGREGATE_TRANSPOSE",
                        $"filter on group key over Aggregate(keys={agg.GroupKeys.Count}, aggs={agg.Aggregates.Count})"));
            }),

        new(
            "JOIN_PUSH_TRANSITIVE_PREDICATES",
            "Inner `Join` where one side carries a filter conjunct on an equi-key column and the " +
            "other side carries no filter on its matching key column — so `a.k = b.k AND a.k <pred>` " +
            "fails to imply `b.k <pred>`. Only immediate `Filter` children of each side are inspected " +
            "(under-counts deeper predicates); the mirrored-predicate check is by key column, not by " +
            "expression equality (may over-count when the other side is already constrained differently).",
            (n, _) =>
            {
                if (n is not JoinPlan { JoinType: DbspNet.Sql.Parser.Ast.JoinType.Inner } j || j.EquiKeys.Count == 0)
                {
                    return Array.Empty<Site>();
                }

                var leftPred = SideConstrainedColumns(j.Left);
                var rightPred = SideConstrainedColumns(j.Right);
                var sites = new List<Site>();
                foreach (var k in j.EquiKeys)
                {
                    // Right index is in join-output space: [left..., right...].
                    var rightLocal = k.RightIndex - j.Left.Schema.Count;
                    var leftHas = leftPred.Contains(k.LeftIndex);
                    var rightHas = rightLocal >= 0 && rightPred.Contains(rightLocal);
                    if (leftHas != rightHas)
                    {
                        sites.Add(new Site("JOIN_PUSH_TRANSITIVE_PREDICATES",
                            $"equi-key {(leftHas ? "left" : "right")} side filtered, other side unconstrained"));
                    }
                }

                return sites;
            }),

        new(
            "JOIN_DERIVE_IS_NOT_NULL_FILTER",
            "Inner `Join` with a *nullable* equi-key column type: the join drops NULL-key rows anyway " +
            "(`AllowNullKeys=false`), but they still enter the side's arrangement, so an `IS NOT NULL` " +
            "filter pushed below keeps them out of the trace. Counts one site per nullable key column. " +
            "Under-counts nothing; over-counts when an `IS NOT NULL` already sits deeper than the " +
            "immediate child.",
            (n, _) =>
            {
                if (n is not JoinPlan { JoinType: DbspNet.Sql.Parser.Ast.JoinType.Inner } j)
                {
                    return Array.Empty<Site>();
                }

                return j.EquiKeys
                    .Where(k => k.KeyType.Nullable)
                    .Select(k => new Site("JOIN_DERIVE_IS_NOT_NULL_FILTER",
                        $"nullable equi-key ({k.KeyType.Display}) on inner join"));
            }),

        new(
            "REDUCE_EXPRESSIONS",
            "A constant-foldable expression node — an all-literal `Binary`/`Unary`/`Cast` subtree, or a " +
            "`CASE` arm with a literal condition — surviving into the compiled plan, where it is " +
            "re-evaluated per row per tick. Scanned over Filter predicates, Project items, Join " +
            "residuals/keys and Aggregate arguments (under-counts: window/TopK key expressions).",
            (n, _) => OwnExpressions(n)
                .SelectMany(Subexpressions)
                .Where(IsFoldable)
                .Select(e => new Site("REDUCE_EXPRESSIONS", $"foldable `{Describe(e)}` in {n.GetType().Name}"))),

        new(
            "PROJECT_AGGREGATE_MERGE",
            "`Project` over `Aggregate` that never references some aggregate output column — the " +
            "aggregate computes and *retains per-group state* for a value nothing reads. Counts one " +
            "site per dead aggregate call. Under-counts: dead aggregates behind an unfused projection.",
            (n, _) =>
            {
                if (n is not ProjectPlan { Input: AggregatePlan agg } p)
                {
                    return Array.Empty<Site>();
                }

                var used = new HashSet<int>();
                foreach (var item in p.Projections)
                {
                    used.UnionWith(ExpressionRewriter.CollectColumnIndices(item.Expression));
                }

                return Enumerable.Range(0, agg.Aggregates.Count)
                    .Where(i => !used.Contains(agg.GroupKeys.Count + i))
                    .Select(i => new Site("PROJECT_AGGREGATE_MERGE",
                        $"unreferenced {agg.Aggregates[i].Kind} in Aggregate(aggs={agg.Aggregates.Count})"));
            }),

        new(
            "AGGREGATE_CASE_TO_FILTER",
            "`SUM`/`COUNT` over a `CASE WHEN p THEN e ELSE <null|0> END` — the analytic idiom for a " +
            "filtered aggregate. Rewriting to a filtered aggregate lets the row skip the aggregator " +
            "entirely instead of contributing a zero.",
            (n, _) =>
            {
                if (n is not AggregatePlan agg)
                {
                    return Array.Empty<Site>();
                }

                return agg.Aggregates
                    .Where(a => a.Kind is AggregateKind.Sum or AggregateKind.Count
                        && a.Argument is ResolvedCaseWhen { Whens.Count: 1 } c
                        && (c.ElseResult is null || c.ElseResult is ResolvedLiteral))
                    .Select(a => new Site("AGGREGATE_CASE_TO_FILTER", $"{a.Kind}(CASE WHEN … END)"));
            }),

        new(
            "AGGREGATE_REMOVE_DUPLICATE_KEYS",
            "`Aggregate` with two structurally-identical group-key expressions — a wider key tuple " +
            "than the grouping needs, paid on every keyed lookup and stored in every trace entry.",
            (n, _) =>
            {
                if (n is not AggregatePlan agg || agg.GroupKeys.Count < 2)
                {
                    return Array.Empty<Site>();
                }

                var seen = new List<ResolvedExpression>();
                var sites = new List<Site>();
                foreach (var k in agg.GroupKeys)
                {
                    if (seen.Any(s => s.Equals(k)))
                    {
                        sites.Add(new Site("AGGREGATE_REMOVE_DUPLICATE_KEYS", $"duplicate group key `{Describe(k)}`"));
                    }
                    else
                    {
                        seen.Add(k);
                    }
                }

                return sites;
            }),

        new(
            "UNION_MERGE",
            "`UnionAll` with a `UnionAll` branch — a nesting the compiler lowers to one operator per " +
            "level. Flattening removes a whole stream hop per level (the HOP lowering builds these).",
            (n, _) => n is UnionAllPlan u
                ? u.Branches.OfType<UnionAllPlan>()
                    .Select(b => new Site("UNION_MERGE", $"nested UnionAll({b.Branches.Count}) inside UnionAll({u.Branches.Count})"))
                : Array.Empty<Site>()),

        new(
            "PROJECT_REMOVE",
            "A trivial identity `Project` — one item per input column, each a bare `ResolvedColumn(i)` " +
            "in order — which still compiles to a per-row `MapRows`.",
            (n, _) =>
            {
                if (n is not ProjectPlan p || p.Projections.Count != p.Input.Schema.Count)
                {
                    return Array.Empty<Site>();
                }

                var identity = p.Projections
                    .Select((item, i) => item.Expression is ResolvedColumn c && c.Index == i)
                    .All(x => x);
                return identity
                    ? new[] { new Site("PROJECT_REMOVE", $"identity Project({p.Projections.Count} cols)") }
                    : Array.Empty<Site>();
            }),

        new(
            "JOIN_TO_SEMI_JOIN",
            "Inner `Join` whose parent `Project` reads no column from the right side and whose residual " +
            "is empty — the right side exists only to filter. A semi-join keeps the right payload out of " +
            "the output *and* out of the join's arrangement. Over-counts: soundness additionally needs " +
            "the right side distinct on the key (no statistics here to prove it), so these are candidate " +
            "sites pending a distinctness check.",
            (n, parent) =>
            {
                if (n is not JoinPlan { JoinType: DbspNet.Sql.Parser.Ast.JoinType.Inner, Residual: null } j
                    || parent is not ProjectPlan p)
                {
                    return Array.Empty<Site>();
                }

                var used = new HashSet<int>();
                foreach (var item in p.Projections)
                {
                    used.UnionWith(ExpressionRewriter.CollectColumnIndices(item.Expression));
                }

                var leftCount = j.Left.Schema.Count;
                var readsRight = used.Any(i => i >= leftCount);
                return readsRight
                    ? Array.Empty<Site>()
                    : new[] { new Site("JOIN_TO_SEMI_JOIN", $"right side ({j.Right.Schema.Count} cols) unread above the join") };
            }),

        new(
            "AGGREGATE_NARROWING_BLOCKED_BY_MINMAX",
            "Not a Calcite rule — the existing `NarrowAggregateInput` bailing out because the call list " +
            "contains MIN/MAX (non-linear in Z-set weights), while the input is wider than the aggregate " +
            "reads. Censused here because Calcite's `AGGREGATE_REDUCE_FUNCTIONS` family is the lever that " +
            "would unblock it, and its site count sizes that lever.",
            (n, _) =>
            {
                if (n is not AggregatePlan agg
                    || !agg.Aggregates.Any(a => a.Kind is AggregateKind.Min or AggregateKind.Max))
                {
                    return Array.Empty<Site>();
                }

                var referenced = new HashSet<int>();
                foreach (var k in agg.GroupKeys)
                {
                    referenced.UnionWith(ExpressionRewriter.CollectColumnIndices(k));
                }

                foreach (var a in agg.Aggregates.Where(a => a.Argument is not null))
                {
                    referenced.UnionWith(ExpressionRewriter.CollectColumnIndices(a.Argument!));
                }

                return referenced.Count < agg.Input.Schema.Count
                    ? new[]
                    {
                        new Site("AGGREGATE_NARROWING_BLOCKED_BY_MINMAX",
                            $"MIN/MAX aggregate reads {referenced.Count} of {agg.Input.Schema.Count} input columns"),
                    }
                    : Array.Empty<Site>();
            }),
    };

    private static IEnumerable<Site> Census(LogicalPlan root)
    {
        var seen = new HashSet<LogicalPlan>(ReferenceEqualityComparer.Instance);
        var stack = new Stack<(LogicalPlan Node, LogicalPlan? Parent)>();
        stack.Push((root, null));
        while (stack.Count > 0)
        {
            var (node, parent) = stack.Pop();
            if (!seen.Add(node))
            {
                continue; // reference-shared subtree: one rewrite site, not two.
            }

            foreach (var d in Detectors)
            {
                foreach (var site in d.Match(node, parent))
                {
                    yield return site;
                }
            }

            foreach (var child in Children(node))
            {
                stack.Push((child, node));
            }
        }
    }

    // ---------- expression helpers ----------

    /// <summary>The expressions a plan node owns directly (not its children's).</summary>
    private static IEnumerable<ResolvedExpression> OwnExpressions(LogicalPlan plan)
    {
        switch (plan)
        {
            case FilterPlan f:
                yield return f.Predicate;
                break;
            case ProjectPlan p:
                foreach (var item in p.Projections)
                {
                    yield return item.Expression;
                }

                break;
            case JoinPlan j:
                if (j.Residual is not null)
                {
                    yield return j.Residual;
                }

                break;
            case AggregatePlan a:
                foreach (var k in a.GroupKeys)
                {
                    yield return k;
                }

                foreach (var call in a.Aggregates.Where(c => c.Argument is not null))
                {
                    yield return call.Argument!;
                }

                break;
            case SemiJoinPlan sj:
                foreach (var k in sj.EquiKeys)
                {
                    yield return k.OuterKey;
                }

                break;
        }
    }

    private static IEnumerable<ResolvedExpression> Subexpressions(ResolvedExpression e)
    {
        yield return e;
        switch (e)
        {
            case ResolvedBinary b:
                foreach (var s in Subexpressions(b.Left).Concat(Subexpressions(b.Right)))
                {
                    yield return s;
                }

                break;
            case ResolvedUnary u:
                foreach (var s in Subexpressions(u.Operand))
                {
                    yield return s;
                }

                break;
            case ResolvedIsNull i:
                foreach (var s in Subexpressions(i.Operand))
                {
                    yield return s;
                }

                break;
            case ResolvedCast c:
                foreach (var s in Subexpressions(c.Operand))
                {
                    yield return s;
                }

                break;
            case ResolvedFunctionCall fc:
                foreach (var s in fc.Arguments.SelectMany(Subexpressions))
                {
                    yield return s;
                }

                break;
            case ResolvedInList il:
                foreach (var s in Subexpressions(il.Probe))
                {
                    yield return s;
                }

                break;
            case ResolvedCaseWhen cw:
                foreach (var w in cw.Whens)
                {
                    foreach (var s in Subexpressions(w.Condition).Concat(Subexpressions(w.Result)))
                    {
                        yield return s;
                    }
                }

                if (cw.ElseResult is not null)
                {
                    foreach (var s in Subexpressions(cw.ElseResult))
                    {
                        yield return s;
                    }
                }

                break;
        }
    }

    /// <summary>
    /// A node that is <i>itself</i> reducible: a compound expression all of whose
    /// leaves are literals (so it evaluates to a constant), or a CASE arm whose
    /// condition is a literal. Bare literals are not sites — they are already folded.
    /// </summary>
    private static bool IsFoldable(ResolvedExpression e) => e switch
    {
        ResolvedBinary or ResolvedUnary or ResolvedCast or ResolvedIsNull => AllLiteralLeaves(e),
        ResolvedCaseWhen cw => cw.Whens.Any(w => w.Condition is ResolvedLiteral),
        _ => false,
    };

    private static bool AllLiteralLeaves(ResolvedExpression e) => e switch
    {
        ResolvedLiteral => true,
        ResolvedBinary b => AllLiteralLeaves(b.Left) && AllLiteralLeaves(b.Right),
        ResolvedUnary u => AllLiteralLeaves(u.Operand),
        ResolvedCast c => AllLiteralLeaves(c.Operand),
        ResolvedIsNull i => AllLiteralLeaves(i.Operand),
        _ => false,
    };

    /// <summary>Column indices constrained by a filter sitting directly on a join input.</summary>
    private static HashSet<int> SideConstrainedColumns(LogicalPlan side)
    {
        var result = new HashSet<int>();
        if (side is FilterPlan f)
        {
            foreach (var c in ExpressionRewriter.SplitAnd(f.Predicate))
            {
                result.UnionWith(ExpressionRewriter.CollectColumnIndices(c));
            }
        }

        return result;
    }

    private static string Describe(ResolvedExpression e) => e switch
    {
        ResolvedLiteral l => l.Value?.ToString() ?? "NULL",
        ResolvedColumn c => $"#{c.Index}",
        ResolvedBinary b => $"{Describe(b.Left)} {b.Operator} {Describe(b.Right)}",
        ResolvedUnary u => $"{u.Operator} {Describe(u.Operand)}",
        ResolvedCast c => $"CAST({Describe(c.Operand)} AS {c.Type.Name})",
        ResolvedIsNull i => $"{Describe(i.Operand)} IS {(i.Negated ? "NOT " : "")}NULL",
        ResolvedFunctionCall f => $"{f.FunctionName}(…)",
        ResolvedCaseWhen => "CASE …",
        _ => e.GetType().Name,
    };

    // ---------- plumbing ----------

    private static LogicalPlan BuildOptimized(string[] ddl, string sql)
    {
        var catalog = new Catalog();
        var resolver = new Resolver(catalog);
        foreach (var s in ddl)
        {
            resolver.Resolve(Parser.ParseStatement(s));
        }

        var plan = ((SelectPlan)resolver.Resolve(Parser.ParseStatement(sql))).Query;
        return PlanOptimizer.Optimize(plan);
    }

    // Child plans via reflection, mirroring NexmarkPlanDump: every property typed
    // as LogicalPlan or IEnumerable<LogicalPlan>, in declaration order.
    private static IEnumerable<LogicalPlan> Children(LogicalPlan plan)
    {
        foreach (var p in plan.GetType().GetProperties(BindingFlags.Public | BindingFlags.Instance))
        {
            if (typeof(LogicalPlan).IsAssignableFrom(p.PropertyType))
            {
                if (p.GetValue(plan) is LogicalPlan child)
                {
                    yield return child;
                }
            }
            else if (p.PropertyType != typeof(string) && typeof(IEnumerable).IsAssignableFrom(p.PropertyType))
            {
                if (p.GetValue(plan) is IEnumerable seq)
                {
                    foreach (var item in seq)
                    {
                        if (item is LogicalPlan child)
                        {
                            yield return child;
                        }
                    }
                }
            }
        }
    }

    private static string FirstLine(string s)
    {
        var i = s.IndexOf('\n');
        return i < 0 ? s : s[..i];
    }
}
